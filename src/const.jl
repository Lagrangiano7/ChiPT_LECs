function fun_beta(ens::String)
    if ens[2] == '6'
        return 3.34
    elseif ens[2] == '1'
        return 3.40
    elseif ens[2] == '4'
        return 3.46
    elseif ens[2] == '2'
        return 3.55
    elseif ens[2] == '3'
        return 3.70
    elseif ens[2] == '5'
        return 3.85
    end
end

function ens_sym_find(beta::Vector{Float64})
    ens_sym = []
    push!(ens_sym, findfirst(x -> x == 3.34, beta))
    push!(ens_sym, findfirst(x -> x == 3.40, beta))
    push!(ens_sym, findfirst(x -> x == 3.46, beta))
    push!(ens_sym, findfirst(x -> x == 3.55, beta))
    push!(ens_sym, findfirst(x -> x == 3.70, beta))
    push!(ens_sym, findfirst(x -> x == 3.85, beta))
    return ens_sym
end

function ens_various(beta::Vector{Float64})
    ens_334 = findall(x -> x == 3.34, beta)
    ens_340 = findall(x -> x == 3.40, beta)
    ens_346 = findall(x -> x == 3.46, beta)
    ens_355 = findall(x -> x == 3.55, beta)
    ens_370 = findall(x -> x == 3.70, beta)
    ens_385 = findall(x -> x == 3.85, beta)
    ens_beta = [ens_334, ens_340, ens_346, ens_355, ens_370, ens_385]
    ens_sym = ens_sym_find(beta)
    ens_sym_tot = [[ens_sym[j] for i in eachindex(ens_beta[j])] for j in eachindex(ens_beta)]
    ens_sym_tot = vcat(ens_sym_tot...)
    ens_nosym = setdiff(eachindex(beta), ens_sym)
    return ens_beta, ens_sym, ens_sym_tot, ens_nosym
end

